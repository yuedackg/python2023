# 関数について
# 関数は、xxxx()の形をしたもの。
# 例）　print()、input()、sqrt()などがある
# 今回は絶対値を計算するabs()は符号を取ったもの
print( abs(  100 ))
print( abs( -100 ))

# abs()　の括弧の前の文字を関数名　　　　　　　　：abs
# 		　括弧の中の値を引数（ひきすう）という　：100

c = abs( -100 )
print( c )

# abs(-100)の結果が、変数ｃに格納される。
# この時、計算された値を「戻り値」という　：戻り値は100

